package com.demo.entity.Case;

import java.util.Date;

public class CasePojo {
     private int id;
     private String img;
     private Date time;
     public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
	
     
}
